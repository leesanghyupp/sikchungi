const SORTED_BY_LENGTH = [...FOOD_NAMES].sort((a, b) => b.length - a.length);

export const detectDirectFood = (text: string): string | null => {
  if (!text) return null;
  for (const name of SORTED_BY_LENGTH) {
    if (text.includes(name)) return name;
  }
  return null;
};
